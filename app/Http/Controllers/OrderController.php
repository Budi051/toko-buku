    public function form_submit_front()
    {
        //$this->authorize('checkmember');

        $cart = session()->get('cart');
        $user = Auth::user();

        $t = new Order();
        $t->user_id = $user->id;
        $t->save();

        $total_harga = $t->insertProduct($cart, $user);
        $t->total_belanja = $total_harga;
        $t->save();

        session()->forget('cart');
        return redirect('/home');
    }
